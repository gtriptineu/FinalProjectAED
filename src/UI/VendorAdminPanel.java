private void populateTable() {
        VendorDirectory pDir = new VendorDirectory();
        VendorDAOImp phDao = new VendorDAOImp();
        pDir = phDao.getAll();
        
        DefaultTableModel model = (DefaultTableModel) vendorTable.getModel();
        model.setRowCount(0);
         
         for(Vendor p: pDir.getVendorDir())
         {
             Object[] row = new Object[4];
             row[0]=p.getName();
             row[1]=p;
             row[2]=p.getCommunity();
             row[3] = p.getContactNo();
             model.addRow(row);
         }
}
