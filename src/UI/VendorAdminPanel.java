    private void findBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtn1ActionPerformed
        // TODO add your handling code here:
        String name = nameTxtField.getText();
        String email = emailTxtField.getText();
        String community = communityDropbox.getSelectedItem().toString();
        String password = String.valueOf(passwordTxtField1.getPassword());
        String contact = contactTextField1.getText();
        String address = addressTxtfield.getText();
        if (name.isEmpty() || email.isEmpty() ||
            community.equals("select community")  || password.isEmpty() ||
            contact.isEmpty() || address.isEmpty()){
           javax.swing.JOptionPane.showMessageDialog(null,"Please Enter all fields");
           
        }
        else {
            
        Vendor v = new Vendor();
        v.setEmail(email);
        v.setName(name);
        v.setAddress(address);
        v.setCommunity(community);
        v.setPassword(password);
        v.setContactNo(contact);
        
        VendorDAOImp vDao = new VendorDAOImp();
        vDao.add(v);
  
        javax.swing.JOptionPane.showMessageDialog(this, "Vendor details are added.");
        populateTable();
        nameTxtField.setText("");
        emailTxtField.setText("");
        communityDropbox.setSelectedItem("select community");
        passwordTxtField1.setText("");
        contactTextField1.setText("");
        addressTxtfield.setText("");

            

        }

    }//GEN-LAST:event_findBtn1ActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = vendorTable.getSelectedRow();
        if(selectedRowIndex < 0){
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a row to be deleted.");
        } else {
            DefaultTableModel table = (DefaultTableModel) vendorTable.getModel();
            Vendor inv = (Vendor)table.getValueAt(selectedRowIndex, 1);
            VendorDAOImp invDao = new VendorDAOImp();
            invDao.delete(inv);
            javax.swing.JOptionPane.showMessageDialog(this, "Medicine Id: "+ inv.getEmail()+ " is deleted.");
            populateTable();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

